public class Zadatak16 {

	public static void main(String[] args) {
		
		prime(100);
		
	}
	
	public static void prime(int unos) {
		
		for(int i = 0; i < unos; i++) {
			if((i + 2) - i == 2) {
				System.out.println(i + " " + (i + 2));
			}
			
			
		}
		
		
	}

}
